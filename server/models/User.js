import mongoose from "mongoose";

// Kullanıcı Şeması
const UserSchema = new mongoose.Schema(
  {
    firstName: {
      type: String,
      required: true,
      min: 2,
      max: 50,
    },
    lastName: {
      type: String,
      required: true,
      min: 2,
      max: 50,
    },
    email: {
      type: String,
      required: true,
      max: 50,
      unique: true,
    },
    password: {
      type: String,
      required: true,
      min: 5,
    },
    picturePath: {
      type: String,
      default: "",
    },
    friends: {
      type: [String], // Arkadaşlar dizisi olarak tanımlandı
      default: [],
    },
    location: String,
    occupation: String,
    ViewedProfile: {
      type: Number,
      default: 0,
    },
    impressions: {
      type: Number,
      default: 0,
    },
  },
  { timestamps: true }
);

// Kullanıcı Modeli
const User = mongoose.model("User", UserSchema);

export default User;
